class LoadUnloadJson {
  final int? pagecount;
  final int? recordssize;
  final int? skiprecords;
  final int? rowcount;
  final List<Records>? records;

  LoadUnloadJson({
    this.pagecount,
    this.recordssize,
    this.skiprecords,
    this.rowcount,
    this.records,
  });

  LoadUnloadJson.fromJson(Map<String, dynamic> json)
      : pagecount = json['page-count'] as int?,
        recordssize = json['records-size'] as int?,
        skiprecords = json['skip-records'] as int?,
        rowcount = json['row-count'] as int?,
        records = (json['records'] as List?)
            ?.map((dynamic e) => Records.fromJson(e as Map<String, dynamic>))
            .toList();

  Map<String, dynamic> toJson() => {
        'page-count': pagecount,
        'records-size': recordssize,
        'skip-records': skiprecords,
        'row-count': rowcount,
        'records': records?.map((e) => e.toJson()).toList()
      };
}

class Records {
  final int? id;
  final String? uid;
  final ADClientID? aDClientID;
  final ADOrgID? aDOrgID;
  final bool? isActive;
  final String? created;
  final CreatedBy? createdBy;
  final String? updated;
  final UpdatedBy? updatedBy;
  final String? documentNo;
  final String? description;
  final String? movementDate;
  final bool? processed;
  final bool? processing;
  final MWarehouseID? mWarehouseID;
  final CActivityID? cActivityID;
  final int? approvalAmt;
  final DocStatus? docStatus;
  final bool? isApproved;
  final CDocTypeID? cDocTypeID;
  final double? processedOn;
  final String? modelname;

  Records({
    this.id,
    this.uid,
    this.aDClientID,
    this.aDOrgID,
    this.isActive,
    this.created,
    this.createdBy,
    this.updated,
    this.updatedBy,
    this.documentNo,
    this.description,
    this.movementDate,
    this.processed,
    this.processing,
    this.mWarehouseID,
    this.cActivityID,
    this.approvalAmt,
    this.docStatus,
    this.isApproved,
    this.cDocTypeID,
    this.processedOn,
    this.modelname,
  });

  Records.fromJson(Map<String, dynamic> json)
      : id = json['id'] as int?,
        uid = json['uid'] as String?,
        aDClientID = (json['AD_Client_ID'] as Map<String, dynamic>?) != null
            ? ADClientID.fromJson(json['AD_Client_ID'] as Map<String, dynamic>)
            : null,
        aDOrgID = (json['AD_Org_ID'] as Map<String, dynamic>?) != null
            ? ADOrgID.fromJson(json['AD_Org_ID'] as Map<String, dynamic>)
            : null,
        isActive = json['IsActive'] as bool?,
        created = json['Created'] as String?,
        createdBy = (json['CreatedBy'] as Map<String, dynamic>?) != null
            ? CreatedBy.fromJson(json['CreatedBy'] as Map<String, dynamic>)
            : null,
        updated = json['Updated'] as String?,
        updatedBy = (json['UpdatedBy'] as Map<String, dynamic>?) != null
            ? UpdatedBy.fromJson(json['UpdatedBy'] as Map<String, dynamic>)
            : null,
        documentNo = json['DocumentNo'] as String?,
        description = json['Description'] as String?,
        movementDate = json['MovementDate'] as String?,
        processed = json['Processed'] as bool?,
        processing = json['Processing'] as bool?,
        mWarehouseID = (json['M_Warehouse_ID'] as Map<String, dynamic>?) != null
            ? MWarehouseID.fromJson(
                json['M_Warehouse_ID'] as Map<String, dynamic>)
            : null,
        cActivityID = (json['C_Activity_ID'] as Map<String, dynamic>?) != null
            ? CActivityID.fromJson(
                json['C_Activity_ID'] as Map<String, dynamic>)
            : null,
        approvalAmt = json['ApprovalAmt'] as int?,
        docStatus = (json['DocStatus'] as Map<String, dynamic>?) != null
            ? DocStatus.fromJson(json['DocStatus'] as Map<String, dynamic>)
            : null,
        isApproved = json['IsApproved'] as bool?,
        cDocTypeID = (json['C_DocType_ID'] as Map<String, dynamic>?) != null
            ? CDocTypeID.fromJson(json['C_DocType_ID'] as Map<String, dynamic>)
            : null,
        processedOn = json['ProcessedOn'] as double?,
        modelname = json['model-name'] as String?;

  Map<String, dynamic> toJson() => {
        'id': id,
        'uid': uid,
        'AD_Client_ID': aDClientID?.toJson(),
        'AD_Org_ID': aDOrgID?.toJson(),
        'IsActive': isActive,
        'Created': created,
        'CreatedBy': createdBy?.toJson(),
        'Updated': updated,
        'UpdatedBy': updatedBy?.toJson(),
        'DocumentNo': documentNo,
        'Description': description,
        'MovementDate': movementDate,
        'Processed': processed,
        'Processing': processing,
        'M_Warehouse_ID': mWarehouseID?.toJson(),
        'C_Activity_ID': cActivityID?.toJson(),
        'ApprovalAmt': approvalAmt,
        'DocStatus': docStatus?.toJson(),
        'IsApproved': isApproved,
        'C_DocType_ID': cDocTypeID?.toJson(),
        'ProcessedOn': processedOn,
        'model-name': modelname
      };
}

